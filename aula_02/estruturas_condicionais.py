# No Brasil, para alguém dirigir a pessoa precisa de 18 anos.
# Se a pesso a tiver a partir de 18 anos, pode dirigir 
# Senão, não pode dirigir 

idade = 16
habilitacao = False 

if idade >= 18:
    if habilitacao == True:
        print("Você pode dirigir ")
    else:
        print("Você não possui habilitacao")
else:
    print("Você não tem idade o suficiente para dirigir!")



